#include "libft.h"

int	ft_word_count(char *s, char c)
{
	int i;
	int words;

	i = 0;
	words = 0;
	while (s[i])
	{
		if ((s[i] != c) && (i == 0 || s[i - 1] == c))
			words++;
		i++;
	}
	return (words);
}
