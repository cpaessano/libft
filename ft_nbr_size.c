#include "libft.h"

int	ft_nbr_size(int i)
{
	int		size;
	long	n;

	size = 0;
	n = (long)i;
	if (n <= 0)
	{
		size++;
		n = -n;
	}
	while (n)
	{
		size++;
		n = n / 10;
	}
	return (size);
}
